#define SETBIT(WORD,BITPOS) WORD|=(1<<BITPOS)
#define SETBYT(WORD,BITPOS) WORD|=(0XFF<<BITPOS)
#define SSETBIT(WORD,BITPOS) WORD=(1<<BITPOS)
#define CLRBIT(WORD,BITPOS) WORD&=(~(1<<BITPOS))
#define SCLRBIT SSETBIT
#define CPLBIT(WORD,BITPOS) WORD^=(1<<BITPOS)
#define READBIT(WORD,BITPOS) ((WORD>>BITPOS)&1)
#define READNIBBLE(WORD,SBITPOS) ((WORD>>SBITPOS)&15)
#define WRITEBIT(WORD,BITPOS,BIT) WORD=(WORD&(~(1<<BITPOS)))|(BIT<<BITPOS)
#define WRITEPAIRBIT(WORD,BITPAIRPOS,PAIRBIT) WORD=(WORD&(~(3<<BITPAIRPOS)))|(PAIRBIT<<BITPAIRPOS)
#define WRITENIBBLE(WORD,SBITPOS,NIBBLE) WORD=(WORD&(~(15<<SBITPOS)))|(NIBBLE<<SBITPOS)
#define WRITEBYTE(WORD,SBITPOS,BYTE) WORD=(WORD&(~(255<<SBITPOS)))|(BYTE<<SBITPOS)
#define READWRITEBIT2(DWORD,WBIT,SWORD,RBIT) DWORD=(DWORD&(~(1<<WBIT)))|(((SWORD>>RBIT)&1)<<WBIT)
#define READWRITEBIT(WORD,WBIT,RBIT) WORD=(WORD&(~(1<<WBIT)))|(((WORD>>RBIT)&1)<<WBIT)
#define CFGPIN(WORD,PIN,FUNC) WORD=(PIN<16)?((WORD&(~(3<<(PIN*2))))|(FUNC<<(PIN*2))):((WORD&(~(3<<((PIN-16)*2))))|(FUNC<<((PIN-16)*2)))
#define FUN1 0
#define FUN2 1
